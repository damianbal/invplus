    public function showPDF(Invoice $invoice)
    {
        $client = $invoice->client;
      $user = $invoice->user;
      $items = $invoice->items;

        $pdf = app('dompdf.wrapper');
        $pdf->loadView("pdf.invoice", ['invoice' => $invoice, 'user' => $user, 'items' => $items, 'client' => $client]);
       
        return $pdf->download("inv.pdf");
